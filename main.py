def get_proxy_for_phone(phone):
    """–í—ã–±–∏—Ä–∞–µ–º –ø—Ä–æ–∫—Å–∏ –¥–ª—è –Ω–æ–º–µ—Ä–∞"""
    # –î–ª—è —Å—Ç–∞—Ä—ã—Ö —Å–µ—Å—Å–∏–π - –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å–æ—Ö—Ä–∞–Ω–µ–Ω–Ω—ã–π proxy_index –∏–ª–∏ 0 (–æ—Å–Ω–æ–≤–Ω–æ–π –ø—Ä–æ–∫—Å–∏)
    existing_session = sessions_col.find_one({"phone": phone})
    if existing_session:
        # –ï—Å–ª–∏ —É —Å–µ—Å—Å–∏–∏ –Ω–µ—Ç proxy_index, —Å—á–∏—Ç–∞–µ–º —á—Ç–æ –æ–Ω–∞ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç –æ—Å–Ω–æ–≤–Ω—É—é –ø—Ä–æ–∫—Å–∏ (–∏–Ω–¥–µ–∫—Å 0)
        return proxy_list[existing_session.get('proxy_index', 0)]
    
    # –î–ª—è –Ω–æ–≤—ã—Ö —Å–µ—Å—Å–∏–π - —Ä–∞—Å–ø—Ä–µ–¥–µ–ª—è–µ–º –º–µ–∂–¥—É –ø—Ä–æ–∫—Å–∏
    return proxy_list[hash(phone) % len(proxy_list)]

def log_user_action(user_id: int, action: str, data: dict = None):
    """–õ–æ–≥–∏—Ä—É–µ—Ç –¥–µ–π—Å—Ç–≤–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –≤ –±–∞–∑—É –¥–∞–Ω–Ω—ã—Ö"""
    try:
        user_data = {
            "user_id": user_id,
            "action": action,
            "timestamp": datetime.now(),
            "data": data or {}
        }
        start_col.insert_one(user_data)
    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–∏ –¥–µ–π—Å—Ç–≤–∏—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è: {e}")

